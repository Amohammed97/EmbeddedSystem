void Gpt_NotificationFn(void)
{
		static uint8 count =0;
	static uint8 Led_count =0;
	if(count >= 7){
		Dio_WriteChannel(Dio_Channel_F1 + Led_count	,	STD_LOW);
	}
	else
	{
		Dio_WriteChannel(Dio_Channel_F1 + Led_count	,	STD_HIGH);
	}
	if(count == 10){
		count = 0;
		Led_count = Led_count++ >= 3 ? 0 :Led_count++;
		
	}		
	count++;
}





